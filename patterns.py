def _add_pattern(grid, start_x, start_y, pattern):
    for i, row in enumerate(pattern):
        for j, cell in enumerate(row):
            pos_x = start_x + i
            pos_y = start_y + j
            if 0 <= pos_x < len(grid) and 0 <= pos_y < len(grid[0]):
                if cell == 1:
                    grid[pos_x][pos_y] = 1


def add_glider(grid, x, y):
    glider = [
        [0, 1, 0],
        [0, 0, 1],
        [1, 1, 1]
    ]
    _add_pattern(grid, x, y, glider)


